public class HallModel
{
	public String id, hallName;
	public void setId(String id)
	{
		this.id = id;
	}
	public String getId()
	{
		return id;
	}
	
	public void setHallName(String hallName)
	{
		this.hallName = hallName;
	}
	public String getHallName()
	{
		return hallName;
	}
}